<h3 class="head3_darkred">Contact Us</h3>
<br/>
<p>Ontobee is a collaborative project involving different parties. The source code of the Ontobee software is  developed and maintained by <a href="http://www.hegroup.org/">He Group</a>. However, many Ontobee questions may not directly related to the Ontobee software. Since Ontobee is used as the default ontology linked data server, many questions are related to policies associated with <a href="http://www.obofoundry.org/">OBO Foundry</a> (<em>e.g.</em>, OBO PURL and namespace). </p>
<p>You may  refer to the Ontobee web pages <a href="faqs.php">FAQs</a>, <a href="tutorial/index.php">Tutorial</a>, <a href="links.php">Links</a>, and <a href="references.php">References</a>  for possible answers to your questions. </p>
<p>Depending on the topics you would like to communicate, you want to access different web sources or send  emails to different groups or persons: </p>
<blockquote class="block_generic">
  <p><strong>For Ontobee disucssion</strong>, use use the Ontobee-discuss Google Group.  <a href="https://groups.google.com/forum/#!forum/ontobee-discuss">https://groups.google.com/forum/#!forum/ontobee-discuss</a>. Everyone is welcome to join the Google Group. You can post your message through: </p>
  <ul>
    <li>After login, you can directly post message on the Google group website, or </li>
    <li>Directly send  emails to: <a href="mailto:ontobee-discuss@googlegroups.com" target="welcomeMsg" rel="nofollow">ontobee-discuss@googlegroups.com</a> (no need to log in  Ontobee-discuss Google Group) &nbsp;</li>
  </ul>
  <p><strong>For new feature request or bug report</strong>, please submit to: <a href="https://sourceforge.net/p/ontobee/feature-requests/">https://sourceforge.net/p/ontobee/feature-requests/</a></p>
  <p><strong>For OBO Foundry related questions</strong>, please contact the OBO Foundry Operations Committee:&nbsp;<a href="http://code.google.com/p/obo-foundry-operations-committee/">http://code.google.com/p/obo-foundry-operations-committee/</a>. </p>
