<h3 class="head3_darkred">Contact Us</h3>

<p>Ontobee is a collaborative project involving different parties. The source code of the Ontobee software is  developed and maintained by <a href="http://www.hegroup.org/">He Group</a>. However, many Ontobee questions may not directly related to the Ontobee software. Since Ontobee is used as the default ontology linked data server, many questions are related to policies associated with <a href="http://www.obofoundry.org/">OBO Foundry</a> (<em>e.g.</em>, OBO PURL and namespace). </p>
<p>Depending on the topics you would like to communicate, you want to access different web sources or send  emails to different groups or persons: </p>
