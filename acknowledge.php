<h3 class="head3_darkred">Acknowledgements</h3>
<p> The Ontobee software system has primarily been developed by <a href="http://www.hegroup.org/">He Group</a>, particularly Allen Xiang and Oliver He, at the University of Michigan Medical School, which was supported by  NIH-NIAID grant R01AI081062. The He Group has developed all the code of the Ontobee software (including Ontobeep). </p>
<p>Alan Ruttenberg and Chris Mungall have contributed a lot to this community-based collaborative project by providing  guidance,  comments, suggestions, and discussions. Alan initiated a list of requirements that the Ontobee development could use as guidance. Chris  has set up and maintained   the OBO foundry ontology PURLS and OBO foundry ontology setting file that has been used by Ontobee to automatically retrieve and syncronize with OBO foundry ontologies. </p>
<p>Melanie Courtot recommended Ontobee to OBI group on Oct 05, 2009 (Check related <a href="http://obi-developers.2851539.n2.nabble.com/OBI-browser-and-SVN-access-td3770617.html">email thread</a>). Since then she has also provided valuable discussions, comments, and suggestions.</p>
<p>Valueable comments and  suggestions have also been provided by <span id="internal-source-marker_0.13648349562720385">Jie Zheng, Bjoern Peters, Melissa Haendel, Jonathan Rees, Maryanne Martone, Barry Smith, and many other ontology researchers and developers.</span></p>
<p>Mr. Yue Liu, an experience software developer in the University of Michigan, has volunteered starting summer 2013 to help support the Ontobee software debugging and maintenance. </p>
<p>Mr. Bin Zhao, a graduate student in the School of Information inthe University of Michigan, helped the Ontobee project by generating the Ontobee SPARQL tutorial web page. Bin was supported by a MCubed project funded by the University of Michigan. </p>
<p>We would also like to thank all Ontobee users who have often provided suggestions and feedbacks.</p>
<p>It is noted that the Ontobee development has integrated many suggestions and comments from the community and has been inspired by many previous and existing works. Thanks! </p>
